function add(a,b){
    return a+b;
}
function sub(a,b){
    return a-b;
}
function mul(a,b){
    return a*b;
}
function div(a,b){
    return a/b;
}
function mod(a,b){
    return a%b;
}
console.log(`
    ${div(20,20)},
    ${add(20,20)},
    ${mul(20,20)},
    ${sub(20,20)},
    ${mod(20,20)},
    `);
